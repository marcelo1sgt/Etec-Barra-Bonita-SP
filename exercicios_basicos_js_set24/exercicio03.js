/*Agora faça o contrário. Você fornece a temperatura em graus Fahrenheit, seu programa deve converter para Celsius e exibir na tela a temperatura em graus Celsius*/

var Celsius = parseFloat(prompt("Digite a temperatura em Celsius: "))
var Fahrenheit = 9 / 5 * Celsius + 32;

console.log("A temperatura de " + Celsius + "°C convertida em Fahrenheit é de " + Fahrenheit + "°F.");
