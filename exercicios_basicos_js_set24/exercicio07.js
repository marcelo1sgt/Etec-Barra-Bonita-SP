/*Escreva uma função que retorne a área de um triângulo, a partir dos valores de base e altura fornecidos.*/

var base = parseInt(prompt("Dê-me a medida da base e da altura de um triângulo e retornarei a área do mesmo.\nQual a medida da base do triângulo: "));
var altura = parseInt(prompt("Qual a medida da altura do triângulo: "));

console.log("A área do triângulo é de: " + (base * altura) / 2);